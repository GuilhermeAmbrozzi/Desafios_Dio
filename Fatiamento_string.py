# Fatiamento da string vai retornar pedaços da string veja exemplos:

nome = "Guilherme Aires Ambrozzi"

# Aqui vai pegar somente a primeira letra
print(nome[0])
# Vai contar a string a partir do zero, e mostrar o resultado.
print(nome[:9])
# Aqui como colocamos o : depois vai mostrar somente o que esta após a 10 casa.
print(nome[10:])
# Aqui vai pegar somente o texto entre essas casas.
print(nome[10:16])
# Aqui mostra toda string pois, não foi declarado nada
print(nome[:])
# Aqui para mostrar a string invertida
print(nome[::-1])
